#include "test.h"
//---------------------------------------------------------------------------------------
Test::Test(QObject *parent)
    : QObject{parent}
{}
//---------------------------------------------------------------------------------------
QString Test::name()
{
    return m_name;
}
//---------------------------------------------------------------------------------------
QMap<QString, QString> Test::map()
{
    return m_map;
}
//---------------------------------------------------------------------------------------
void Test::fill()
{
    m_name = "test object";
    for(int i=0; i<10; ++i){
        QString num = QString::number(i);
        QString key = "Key " + num;
        QString value = "This item is " + num;
        m_map.insert(key, value);
    }
}
//---------------------------------------------------------------------------------------
void Test::setName(QString name)
{
    m_name = name;
}
//---------------------------------------------------------------------------------------
void Test::setMap(QMap<QString, QString> newMap)
{
    m_map = newMap;
}
